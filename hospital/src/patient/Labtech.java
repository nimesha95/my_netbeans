    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
        // Add data into the PTEST database
        int pid = Integer.parseInt(P_id.getText());
        int tid= Integer.parseInt(T_id.getText());
        String type = T_type.getText();
        String loc = T_link.getText();
        int empid = Integer.parseInt(emp_id.getText());
        
        String sql ="INSERT INTO PTESTS (pid,tid,tname,date,result,empid) values ("+pid+","+tid+",'"+type+"',now(),'"+loc+"',"+empid+")";
        try {
            DBcon.post(sql);
            JOptionPane.showOptionDialog(null, "Test Documents Submited", "", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
        } catch (Exception ex) {
            Logger.getLogger(Labtech.class.getName()).log(Level.SEVERE, null, ex);
        }
        setTxtNull();
        CardLayout card = (CardLayout)mainP.getLayout();
        card.show(mainP, "landing");
    }//GEN-LAST:event_SubmitBtnActionPerformed

