    private void P_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_P_idKeyReleased
        /*
        This returns the information of the patient when given the patientID for the relevent test
        */
        String p_name="";
        int pID=0;
        try{
            pID =Integer.parseInt(P_id.getText());
        }
        catch(Exception e){
            
        }
        try {
            String sql = "SELECT fname,lname FROM patients WHERE ID="+pID; 
            ResultSet rs = DBcon.get(sql);
            while(rs.next()){
                p_name= rs.getString("fname")+" "+rs.getString("lname");
            }

            rs.close();         
        }
        catch (SQLException | ClassNotFoundException ex) {  
            Logger.getLogger(DBcon.class.getName()).log(Level.SEVERE, null, ex);   
            } catch (Exception ex) {
            Logger.getLogger(Labtech.class.getName()).log(Level.SEVERE, null, ex);
        }
   
        pname.setText(p_name);
                           
    }//GEN-LAST:event_P_idKeyReleased

