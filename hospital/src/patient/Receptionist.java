    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        String varName = (String)Regmeth.getSelectedItem();
        String value = Regmeth.getSelectedItem().toString();    //get selected value from combobox as string
        try {
            String usrID= Reg_ID.getText();
            if (value.equals("RegID")){     //searching database by using register id
                String sql = "SELECT id, Fname, Lname, Address FROM patients WHERE id="+usrID;
                ResultSet rs = DBcon.get(sql);
                while(rs.next()){
                    ReviewRegID.setText(Integer.toString(rs.getInt("id")));
                    ReviewName.setText(rs.getString("Fname")+rs.getString("Lname"));
                    ReviewAddr.setText(rs.getString("Address"));
                }
                rs.close();
                CardLayout card = (CardLayout)mainP.getLayout();
                card.show(mainP, "review");
            }
            else if (value.equals("NIC")){  //searching database by using nic
                String sql = "SELECT id, Fname, Lname, Address FROM patients WHERE NIC="+"'"+usrID+"'";
                ResultSet rs = DBcon.get(sql);
                while(rs.next()){
                    ReviewRegID.setText(Integer.toString(rs.getInt("id")));
                    ReviewName.setText(rs.getString("Fname")+" "+rs.getString("Lname"));
                    ReviewAddr.setText(rs.getString("Address"));
                }
                rs.close();

                CardLayout card = (CardLayout)mainP.getLayout();
                card.show(mainP, "review");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Receptionist.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(Receptionist.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_submitActionPerformed

