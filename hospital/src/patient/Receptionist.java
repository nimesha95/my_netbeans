    private void find_empBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_find_empBtnActionPerformed
        /*
        This finds the employee details from empID to mark attendance
        */
        String emp_id = employee.getText();
        String EmpFname="";
        String EmpLname="";
        String EmpOccupation="";

        String sql = "SELECT FNAME,LNAME,OCCUPATION FROM EMPLOYEE WHERE id="+emp_id;
        try (
            ResultSet rs = DBcon.get(sql)) {
            while (rs.next()) {
                EmpFname= rs.getString("FNAME");
                EmpLname= rs.getString("LNAME");
                EmpOccupation= rs.getString("OCCUPATION");
            }
            System.out.println(EmpFname);
        } catch (Exception ex) {
            Logger.getLogger(Receptionist.class.getName()).log(Level.SEVERE, null, ex);
        }
        if("".equals(EmpFname) || "".equals(EmpLname) || "".equals(EmpOccupation))
        JOptionPane.showMessageDialog(null,"Recheck Employee ID","User Not Found",JOptionPane.ERROR_MESSAGE);
        else{
            empN1.setText(EmpFname+" "+EmpLname);
            empO1.setText(EmpOccupation);
            panelChk.setVisible(true);
        }
        
    }//GEN-LAST:event_find_empBtnActionPerformed

