    private void Attendance_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Attendance_btnActionPerformed
        int todayCou=0;
        String curdate = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(Calendar.getInstance().getTime()) ;
        int calc= Character.getNumericValue(curdate.charAt(9))-1;
        String dayToComp = curdate.substring(0, 9)+calc;
        try {
            String sql = "SELECT COUNT(id) as todayCount FROM ATTENDANCE WHERE DATE>'"+dayToComp+"';";
            ResultSet rs = DBcon.get(sql);
            while(rs.next()){
                todayCou=rs.getInt("todayCount");    
            }
            rs.close();        
        }
        catch (SQLException | ClassNotFoundException ex) {  
            Logger.getLogger(DBcon.class.getName()).log(Level.SEVERE, null, ex);   
            } catch (Exception ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
         JOptionPane.showOptionDialog(null, "Attendance : "+todayCou, "Today Attendance", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
    }//GEN-LAST:event_Attendance_btnActionPerformed

