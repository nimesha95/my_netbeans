    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
        //adds new employee to the system
        Employee emp = new Employee(Fname.getText(),Lname.getText(),addr.getText(),NIC.getText(),Integer.parseInt(phone.getText()),email.getText(),occup.getSelectedItem().toString());
        try {
            emp.addToDB();
        } catch (Exception ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(occup.getSelectedIndex()==0){
            try {
                emp.GetEmpID();
            } catch (Exception ex) {
                Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
            }
            emp.setSpecID(speciality.getSelectedIndex()+1);  //specility id starts from 1
            String sql="INSERT INTO DOCTOR (EMPID,SPECIALTY) VALUES ("+emp.getEmpID()+","+emp.getSpecID()+")";
            try {
                DBcon.post(sql);
            } catch (Exception ex) {
                Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showOptionDialog(null, "Employee Added", "EmpID:"+emp.getEmpID(), JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
        CardLayout card = (CardLayout)mainP.getLayout();
        card.show(mainP, "landing");
    }//GEN-LAST:event_SubmitBtnActionPerformed

