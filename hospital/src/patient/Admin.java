    private void CalcBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcBtnActionPerformed
        // TODO add your handling code here:
        int Attendance=0;
        int Base = 0;
        int Ince =0;
        int pid=Integer.parseInt(Pid.getText());
        DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String StartD = format.format(startDate.getDate());
        String EndD = format.format(EndDate.getDate());

        
        try {
            String sql = "SELECT COUNT(id) as dateCount from ATTENDANCE WHERE EMP_ID="+pid+"AND DATE>'"+StartD+"' AND DATE<'"+EndD+"';";
            ResultSet rs = DBcon.get(sql);
            while(rs.next()){
                Attendance=rs.getInt("dateCount");    
            }
                rs.close();        
        }
        catch (SQLException | ClassNotFoundException ex) {  
            Logger.getLogger(DBcon.class.getName()).log(Level.SEVERE, null, ex);   
            } catch (Exception ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
        //System.out.println(Attendance);
        try{
            Base = Integer.parseInt(BaseSal.getText());
            Ince = Integer.parseInt(Insentive.getText());
            float sal = (float) (((Base/30.0)+(Ince/30.0))*Attendance) ; 
            FinalSal.setText(Float.toString(sal)+" LKR");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Fill all the Fields","",JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_CalcBtnActionPerformed

